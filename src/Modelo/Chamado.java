
package Modelo;


public class Chamado {
    
     int nChamado;
     String nomeEmpresa;
     int idEquipamento;
     String motivoChamada;
     int dataentendimento;

    public int getnChamado() {
        return nChamado;
    }

    public void setnChamado(int nChamado) {
        this.nChamado = nChamado;
    }
     
     

    public String getNomeEmpresa() {
        return nomeEmpresa;
    }

    public void setNomeEmpresa(String nomeEmpresa) {
        this.nomeEmpresa = nomeEmpresa;
    }

    public int getIdEquipamento() {
        return idEquipamento;
    }

    public void setIdEquipamento(int idEquipamento) {
        this.idEquipamento = idEquipamento;
    }

    public String getMotivoChamada() {
        return motivoChamada;
    }

    public void setMotivoChamada(String motivoChamada) {
        this.motivoChamada = motivoChamada;
    }

    public int getDataentendimento() {
        return dataentendimento;
    }

    public void setDataentendimento(int dataentendimento) {
        this.dataentendimento = dataentendimento;
    }
    
    
    
    
}
